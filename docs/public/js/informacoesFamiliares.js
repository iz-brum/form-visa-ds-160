
// Função para mostrar ou ocultar o campo de situação do pai nos EUA
function toggleFatherStatusInUSA() {
    var radioButton = document.getElementById("fatherInUSAYes");
    toggleFields(radioButton, "fatherStatusInUSA", "block");
}

