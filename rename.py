# Passo 2: Função para ler todos os arquivos em um diretório recursivamente
def ler_arquivos_recursivamente(diretorio):
    for raiz, _, arquivos in os.walk(diretorio):
        for arquivo in arquivos:
            if arquivo.endswith(('.html', '.css', '.js')):
                yield os.path.join(raiz, arquivo)

# Passo 3: Função para substituir ocorrências de nomes/IDs antigos pelos novos
def substituir_nomes_ids(caminho_arquivo, mapeamento):
    with open(caminho_arquivo, 'r', encoding='utf-8') as arquivo:
        conteudo = arquivo.read()
    
    for nome_antigo, nome_novo in mapeamento.items():
        conteudo = conteudo.replace(nome_antigo, nome_novo)
    
    with open(caminho_arquivo, 'w', encoding='utf-8') as arquivo:
        arquivo.write(conteudo)

